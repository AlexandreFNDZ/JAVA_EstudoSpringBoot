    private void tabela() {
        int busca = 0;
        boolean alfab = true;
                
        try {
            DefaultTableModel dados = (DefaultTableModel) this.tblDisciplinas.getModel();            
            ArrayList lista = ctrlDisc.buscaDisciplina(busca,alfab);
            
            ctrlDisc.preencheTabela(dados, lista);
        } catch(SQLException ex) {
            ex.printStackTrace();
        }
    }
    
    private void btnIncluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnIncluirActionPerformed
        boolean inseriu = false;

        try {
            inseriu = ctrlDisc.insereDisciplina(Integer.parseInt(this.txtCod.getText()),this.txtDisciplina.getText(),this.txtProf.getText());

            tabela();

        } catch(SQLException ex) {
            ex.printStackTrace();
        } catch(NumberFormatException ex) {
            JOptionPane.showMessageDialog(null, "Preencha os dados Corretamente!");
        }

        if (inseriu) {
            JOptionPane.showMessageDialog(null, "Inserido com Sucesso!");
            this.txtCod.setText("");
            this.txtDisciplina.setText("");
            this.txtProf.setText("");

            this.txtCod.requestFocus();
        }
    }//GEN-LAST:event_btnIncluirActionPerformed

    private void btnExcluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnExcluirActionPerformed
        boolean excluiu = false;

        try {
            int indexDisc = this.tblDisciplinas.getSelectedRow();
            int codInt = Integer.parseInt(this.tblDisciplinas.getValueAt(indexDisc, 0).toString());

            excluiu = ctrlDisc.excluiDisciplina(codInt);

            tabela();
        } catch(SQLException ex) {
            ex.printStackTrace();
        } catch(NumberFormatException ex) {
            JOptionPane.showMessageDialog(null, "Preencha os dados Corretamente!");
        } catch(ArrayIndexOutOfBoundsException ex) {
            JOptionPane.showMessageDialog(null, "Selecione um Aluno na Tabela!");
        }

        if (excluiu) {
            JOptionPane.showMessageDialog(null, "Exclu√≠do com Sucesso!");
            this.txtCod.setText("");
            this.txtDisciplina.setText("");
            this.txtProf.setText("");

            this.txtCod.requestFocus();
        }
    }//GEN-LAST:event_btnExcluirActionPerformed

